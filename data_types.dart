void main() {
  // Integer data type
  int age = 30;

  // Double data type
  double weight = 65.5;

  // String data type
  String name = "Naomi Otieno";

  // List data type (List of integers)
  List<int> numbers = [1, 2, 3, 4, 5];

  // Map data type (Mapping of String keys to int values)
  Map<String, int> scores = {
    'Math': 90,
    'Science': 85,
    'English': 88,
  };

  // Printing values
  print('Age: $age');
  print('Weight: $weight');
  print('Name: $name');
  print('Numbers: $numbers');
  print('Scores: $scores');
}
